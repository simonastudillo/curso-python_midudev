###
# 01 - print()
# El módulo print() es el módulo que nos permite imprimir en consola
# Sirve para mostrar información en consola
###
print("¡Hola Mundo!")
print('Esto tambien funciona con una comilla')

print("python", "es", "un", "lenguaje")
###
# sep  => Parámetro que indica el separador entre cada elemento
# Por defecto es espacio
###
print("python", "es", "genial", sep = "-")
###
# end  => Parametro que indica el final de cada linea
# Por defecto es \n
###
print("Esto se imprime", end = " ")
print("en una linea")