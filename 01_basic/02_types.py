# 02 - Tipos de datos
# Python tiene varios tipos de datos
# int, float, complex, str, bool, NoneType, list, tuple, dict, range, set...
###

print("int")
print(type(10))
print(type(0))
print(type(-5))
print(type(10))
print(type(1365645632186765646546532))

print("float")
print(type(10.0))
print(type(3.14))
print(type(1e3))

print("complex")
print(type(1 + 2j))

print("str")
print(type("hola"))
print(type(""))
print(type("123"))
print(type("""hola
           mundo multinea"""))

print("bool")
print(type(True))
print(type(False))
print(type(1 < 2))

print("NoneType")
print(type(None))